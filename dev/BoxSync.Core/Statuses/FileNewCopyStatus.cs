namespace BoxSync.Core.Statuses
{

	/// <summary>
	/// Specifies statuses returned by 'BoxManager.FileNewCopy' method
	/// </summary>
	public enum FileNewCopyStatus
	{
		/// <summary>
		/// Unknown status string
		/// </summary>
		Unknown = 0,

		/// <summary>
		/// Represents 'upload_ok' status string
		/// </summary>
		Successful = 1,

		/// <summary>
		/// Represents 'upload_some_files_failed' status string
		/// </summary>
		Failed = 2,

		/// <summary>
		/// The user did not successfully authenticate.
		/// Represents 'not_logged_in' status string.
		/// </summary>
		NotLoggedID = 3,

		/// <summary>
		/// An invalid API key was provided, or the API key is restricted from calling this function.
		/// Represents 'application_restricted' status string.
		/// </summary>
		ApplicationRestricted = 4
	}
}
